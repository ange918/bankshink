function getLanguageFromURL() {
    const urlParams = new URLSearchParams(window.location.search);
    const langParam = urlParams.get('lang');
    
    if (langParam) {
        if (translations[langParam]) {
            return langParam;
        } else {
            console.warn('Unsupported language code:', langParam);
        }
    }
    
    const savedLang = localStorage.getItem('preferredLanguage');
    if (savedLang && translations[savedLang]) {
        return savedLang;
    }
    
    return 'fr';
}

function changeLanguage(lang) {
    if (!translations[lang]) {
        console.error('Language not supported:', lang);
        lang = 'fr';
    }
    
    localStorage.setItem('preferredLanguage', lang);
    
    const url = new URL(window.location.href);
    url.searchParams.set('lang', lang);
    
    window.history.pushState({ lang: lang }, '', url.toString());
    
    setLanguage(lang);
    
    const langSelector = document.getElementById('langSelector');
    if (langSelector && langSelector.value !== lang) {
        langSelector.value = lang;
    }
}

function initializeLanguage() {
    const currentLang = getLanguageFromURL();
    
    const langSelector = document.getElementById('langSelector');
    if (langSelector) {
        langSelector.value = currentLang;
        langSelector.addEventListener('change', function() {
            changeLanguage(this.value);
        });
    }
    
    setLanguage(currentLang);
    
    window.addEventListener('popstate', function(event) {
        if (event.state && event.state.lang) {
            const lang = event.state.lang;
            if (translations[lang]) {
                setLanguage(lang);
                if (langSelector) {
                    langSelector.value = lang;
                }
            }
        } else {
            const urlLang = getLanguageFromURL();
            setLanguage(urlLang);
            if (langSelector) {
                langSelector.value = urlLang;
            }
        }
    });
    
    const url = new URL(window.location.href);
    if (!url.searchParams.has('lang')) {
        url.searchParams.set('lang', currentLang);
        window.history.replaceState({ lang: currentLang }, '', url.toString());
    } else {
        window.history.replaceState({ lang: currentLang }, '', window.location.href);
    }
}

